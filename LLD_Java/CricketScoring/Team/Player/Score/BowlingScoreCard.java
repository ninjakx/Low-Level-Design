package Java.CricketScoring.Team.Player.Score;

public class BowlingScoreCard {
    int totalOverSoFar;
    int runsMade;
    int wicketsTaken;
    int noBallCount;
    int wideBallCount;
    public int getTotalOverSoFar() {
        return totalOverSoFar;
    }
    public void setTotalOverSoFar(int totalOverSoFar) {
        this.totalOverSoFar = totalOverSoFar;
    }
    public int getRunsMade() {
        return runsMade;
    }
    public void setRunsMade(int runsMade) {
        this.runsMade = runsMade;
    }
    public int getWicketsTaken() {
        return wicketsTaken;
    }
    public void setWicketsTaken(int wicketsTaken) {
        this.wicketsTaken = wicketsTaken;
    }
    public int getNoBallCount() {
        return noBallCount;
    }
    public void setNoBallCount(int noBallCount) {
        this.noBallCount = noBallCount;
    }
    public int getWideBallCount() {
        return wideBallCount;
    }
    public void setWideBallCount(int wideBallCount) {
        this.wideBallCount = wideBallCount;
    }    
}
